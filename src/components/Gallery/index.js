class Gallery extends Component {
  state = {
    selectedImage: imagesList[0],
  }

  onImageChange = id => {
    const resultImage = imagesList.filter(eachImage => eachImage.id === id)
    this.setState({selectedImage: resultImage[0]})
  }

  render() {
    const {selectedImage} = this.state

    const {imageUrl, imageAltText} = selectedImage
    return (
      <div className="bg-container">
        <div>
          <img src={imageUrl} alt={imageAltText} className="large-image" />
        </div>
        <h1 className="heading">Nature Photography</h1>
        <p className="tag">Nature Photography by Rahul</p>
        <ul className="thumbnail-List">
          {imagesList.map(eachItem => (
            <ThumbnailItem
              eachItem={eachItem}
              key={eachItem.id}
              onImageChange={this.onImageChange}
              activeClass={selectedImage.id === eachItem.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
